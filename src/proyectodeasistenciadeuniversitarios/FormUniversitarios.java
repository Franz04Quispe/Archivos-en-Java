    public void cargarArchivoUniversitario()
    {
       File archi=new File(dirArchivo);//crea un archivo archi
              
       if(!archi.exists())//si el archivo no existe entonces
       try {
          archi.createNewFile();
        } catch (IOException ex) {
          Logger.getLogger(FormUniversitarios.class.getName()).log(Level.SEVERE,null,ex);
        }
        else{
        try{
            FileReader fremp=new FileReader(dirArchivo); 
            BufferedReader br=new BufferedReader(fremp);
            String linea=null;
            //llevamos los datos del archivo al vector
            //lee la primera linea del archivo de texto
            linea=br.readLine(); 
            
            while(linea!=null)
            {
                //separar la cadena linea separado por el token ,
                StringTokenizer st=new StringTokenizer(linea,",");
                univ=new UNIVERSITARIO();
                
                int cod=Integer.parseInt(st.nextToken());
                //int ed=Integer.parseInt(st.nextToken());//añadido------
                univ.setCodigo(cod);
                univ.setNombre(st.nextToken());
                univ.setApellidos(st.nextToken());
                String x=st.nextToken();
                univ.setGenero(x.charAt(1));
                univ.setCargo(st.nextToken());
                univ.setEdad(cod);//añadido
                
                vu.agregarUniversitario(univ);
                
                linea=br.readLine(); 
            }
            br.close();
            
        }catch(Exception ex)
        {
            mensaje("error al cargar el archivo"+ex.getMessage());
        }
}}
    
    public void listarUniversitarios()
    {
        Object fila[]=new Object[dtm.getColumnCount()];
        
        for(int i=0;i<vu.cantidadUniversitario();i++)
        {
            univ=vu.obtenerUniversitario(i);
            fila[0]=univ.getCodigo();
            fila[1]=univ.getNombre();
            fila[2]=univ.getApellidos();
            fila[3]=univ.getGenero();
            fila[4]=univ.getCargo();
            fila[5]=univ.getEdad();//añadido
            dtm.addRow(fila);
        }
        tablaUniversitarios.setModel(dtm);
    }
        
