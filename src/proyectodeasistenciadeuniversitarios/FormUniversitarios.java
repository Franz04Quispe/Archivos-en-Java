    void registrarUniversitario(File dirArchivo)
    {
        try{
            if(leerCodigo()==-99) mensaje("Debe ingresar un número entero");
            else if(leerNombre()==null)mensaje("Debe ingresar el nombre");
            else if(leerApellidos()==null)mensaje("Debe ingresar apellidos");
            else if(leerEdad()==-99) mensaje("Debe ingresar un número entero");
            else
            {
                char genero;
                String cargo;
                if(brMasculino.isSelected())
                    genero='M';
                else genero ='F';
                cargo=(String)cmbCargo.getSelectedItem();
                
                univ=new UNIVERSITARIO(leerCodigo(),leerNombre(),leerApellidos(),genero,cargo,leerEdad());
                if((vu.buscarCodigoUniversitario(univ.getCodigo()))!=-1)
                {
                    mensaje("Este codigo ya existe");
                }
                else{
                    vu.agregarUniversitario(univ);
                }
                grabarUniversitario();
                limpiar();
                listarUniversitarios();
            }
        }catch(Exception ex)
        {
            mensaje("Error al abrir el archivo");
        }
    }
    
    void grabarUniversitario()
    {
        FileWriter fw;
        PrintWriter pw;
        
        try{
            fw=new FileWriter(dirArchivo);
            pw=new PrintWriter(fw);
            for(int i=0;i<vu.cantidadUniversitario();i++)
            {
                univ=vu.obtenerUniversitario(i);
                System.out.println(univ.getGenero());
                pw.println(String.valueOf(univ.getCodigo())+", "+univ.getNombre()+
                        ", "+univ.getApellidos()+", "+
                        univ.getGenero()+", "+univ.getCargo()+", "+String.valueOf(univ.getEdad()));
            }
            pw.close();
        }
        catch (Exception ex)
        {
            mensaje("Error al grabar en el archivo");
        }
    }
    
    void limpiar()
    {
        limpiarTabla();
        txtCodigo.setText(null);
        txtNombre.setText(null);
        txtApellidos.setText(null);
        brMasculino.setSelected(true);
        cmbCargo.setSelectedIndex(0);
        txtEdad.setText(null);
        limpiarTabla(); 
    }
    
