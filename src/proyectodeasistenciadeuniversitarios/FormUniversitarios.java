    private void tablaUniversitariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaUniversitariosMouseClicked
        clickTabla = tablaUniversitarios.rowAtPoint(evt.getPoint());

        int codigo = (int) tablaUniversitarios.getValueAt(clickTabla, 0);
        String nombre = "" + tablaUniversitarios.getValueAt(clickTabla, 1);
        String apellidos = ""+tablaUniversitarios.getValueAt(clickTabla, 2);
        char genero=(char)tablaUniversitarios.getValueAt(clickTabla,3);
        String cargo=""+tablaUniversitarios.getValueAt(clickTabla,4);
        cargo=cargo.trim();
        int edad = (int) tablaUniversitarios.getValueAt(clickTabla, 5);
      
        txtCodigo.setText(String.valueOf(codigo));
        txtNombre.setText(nombre);
        txtApellidos.setText(apellidos);
        if(genero=='M') brMasculino.setSelected(true);
        else brFemenino.setSelected(true);
        txtEdad.setText(String.valueOf(edad));
        
        for(int i=0; i<cmbCargo.getItemCount();i++)
            if(cargo.equals(cmbCargo.getItemAt(i))){
                 cmbCargo.setSelectedIndex(i);
                 break;}
        
    }//GEN-LAST:event_tablaUniversitariosMouseClicked

