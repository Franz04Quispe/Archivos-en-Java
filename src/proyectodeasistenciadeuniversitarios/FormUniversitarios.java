    void modificarUniversitario (File dirArch)
    {
        try{
            if(leerCodigo()==-99)mensaje("Ingresar codigo entero");
            else if(leerNombre()==null)mensaje("Ingresar el nombre");
            else if(leerApellidos()==null)mensaje("Ingresar apellidos");
            else
            {
                int codigo=vu.buscarCodigoUniversitario(leerCodigo());
                univ=new UNIVERSITARIO(leerCodigo(),leerNombre(),leerApellidos(),leerGenero(),leerCargo(),leerEdad());
                if (codigo==-1)
                    vu.agregarUniversitario(univ);
                else{
                    vu.modificarUniversitario(codigo, univ);
                }
                grabarUniversitario();
                limpiar();
                listarUniversitarios();
            }
        }catch(Exception ex){
            mensaje("Error al abrir el archiovo para modificar");
        }
    }
    
    void eliminarUniversitario ()
    {
        try
       {
           if(leerCodigo()==-99) mensaje("ingresar un codigo entero");
           else
           {
               int codigo=vu.buscarCodigoUniversitario(leerCodigo());
               if(codigo==-1)
               {
                   mensaje("El codigo no existe");
               }else {
                   int s=JOptionPane.showConfirmDialog(null,"Esta seguro de "
                           + "eliminar este universitario ","si/no",0);
                   if(s==0)
                   {
                       vu.eliminarUniversitario(codigo);
                       grabarUniversitario();
                       limpiar();
                       listarUniversitarios();
                   }
               }
           }
       }catch(Exception ex){
           mensaje("No se ha podido eliminar el registro");
       }
    }
        
        
